#!/usr/bin/env node
/**
 * sync-changelogs.mjs — generate the docs Changelog pages from each package's
 * own CHANGELOG.md.
 *
 * The docs deploy as a standalone repo, so the generated `.md` files are
 * COMMITTED (CI never sees the sibling packages). Re-run this locally after a
 * release to refresh:  npm run sync:changelogs
 *
 * Missing sources are skipped with a warning and any existing generated page is
 * left untouched, so running this anywhere (incl. CI) is a safe no-op.
 */
import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const DOCS_ROOT = resolve(__dirname, '..');
const PKG_ROOT = resolve(DOCS_ROOT, '../../js-libraries');
const OUT_DIR = join(DOCS_ROOT, 'src/content/docs/changelog');

const GENERATED = '<!-- GENERATED by scripts/sync-changelogs.mjs — do not edit by hand. -->';

const read = (pkg) => {
	const p = join(PKG_ROOT, pkg, 'CHANGELOG.md');
	return existsSync(p) ? readFileSync(p, 'utf8') : null;
};

/** Drop the leading "# Changelog" H1 + intro prose; keep from the first `## ` entry. */
const entries = (md) => {
	const i = md.search(/^## /m);
	return (i === -1 ? md : md.slice(i)).trim();
};

/** Demote ATX headings by `levels`, leaving fenced code blocks alone. */
const demote = (md, levels) => {
	let fence = false;
	return md
		.split('\n')
		.map((line) => {
			if (/^\s*```/.test(line)) fence = !fence;
			const m = !fence && line.match(/^(#{1,6})(\s)/);
			return m ? '#'.repeat(Math.min(6, m[1].length + levels)) + line.slice(m[1].length) : line;
		})
		.join('\n');
};

const frontmatter = ({ title, description, order }) =>
	`---\ntitle: ${title}\ndescription: ${description}\nsidebar:\n  order: ${order}\n---\n`;

// `repo` is the bare GitHub repo name (e.g. `waveform-player`); the npm package
// is the scoped `@arraypress/<repo>`.
const sourceNote = (repo) =>
	`:::note\nGenerated from [\`@arraypress/${repo}\`'s CHANGELOG](https://github.com/arraypress/${repo}/blob/main/CHANGELOG.md). Run \`npm run sync:changelogs\` after a release to refresh.\n:::`;

function write(slug, contents) {
	mkdirSync(OUT_DIR, { recursive: true });
	writeFileSync(join(OUT_DIR, `${slug}.md`), contents.replace(/\n*$/, '\n'));
	console.log(`  ✓ changelog/${slug}.md`);
}

// ── Single-package pages ────────────────────────────────────────────────────
const SINGLE = [
	{ slug: 'player', pkg: 'waveform-player', title: 'Core Player', order: 1, description: 'Release history for @arraypress/waveform-player.' },
	{ slug: 'bar', pkg: 'waveform-bar', title: 'Bar', order: 2, description: 'Release history for @arraypress/waveform-bar.' },
];

for (const { slug, pkg, title, order, description } of SINGLE) {
	const md = read(pkg);
	if (!md) {
		console.warn(`  ⚠ ${pkg}/CHANGELOG.md not found — leaving changelog/${slug}.md as-is.`);
		continue;
	}
	write(slug, [frontmatter({ title, description, order }), GENERATED, '', sourceNote(pkg), '', entries(md)].join('\n'));
}

// ── Combined "Frameworks & wrappers" page ───────────────────────────────────
const WRAPPERS = [
	'waveform-player-astro',
	'waveform-player-react',
	'waveform-bar-astro',
	'waveform-bar-react',
];

const sections = WRAPPERS.map((pkg) => {
	const md = read(pkg);
	if (!md) {
		console.warn(`  ⚠ ${pkg}/CHANGELOG.md not found — omitting from frameworks page.`);
		return null;
	}
	// Demote so each package's `## [x.y.z]` sits under a `## @arraypress/<pkg>` h2.
	return `## \`@arraypress/${pkg}\`\n\n${demote(entries(md), 1)}`;
}).filter(Boolean);

if (sections.length) {
	write('frameworks', [
		frontmatter({ title: 'Framework wrappers', description: 'Release history for the Astro and React wrapper packages.', order: 3 }),
		GENERATED,
		'',
		':::note\nGenerated from each wrapper package\'s CHANGELOG. Run `npm run sync:changelogs` after a release to refresh.\n:::',
		'',
		'The thin Astro/React wrappers version independently of the core libraries they wrap.',
		'',
		sections.join('\n\n'),
	].join('\n'));
}

console.log('Changelog sync complete.');
